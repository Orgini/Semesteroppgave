function show_intro() {
  var intro = document.getElementsByClassName("intro");
  var overview = document.getElementsByClassName("overview");
  var detail = document.getElementsByClassName("detail");
  var compare = document.getElementsByClassName("compare");
  if (intro[0].style.display = "none") {
    intro[0].style.display = "block";
    overview[0].style.display = "none";
    detail[0].style.display = "none";
    compare[0].style.display = "none"
  }

}

